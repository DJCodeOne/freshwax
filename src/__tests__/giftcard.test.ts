// =============================================
// generateGiftCardCode
// =============================================
describe('generateGiftCardCode', () => {
  it('returns a code in FWGC-XXXX-XXXX-XXXX format', () => {
    const code = generateGiftCardCode();
    expect(code).toMatch(/^FWGC-[A-HJ-NP-Z2-9]{4}-[A-HJ-NP-Z2-9]{4}-[A-HJ-NP-Z2-9]{4}$/);
  });

  it('starts with FWGC- prefix', () => {
    const code = generateGiftCardCode();
    expect(code.startsWith('FWGC-')).toBe(true);
  });

  it('has 4 segments separated by hyphens', () => {
    const code = generateGiftCardCode();
    const segments = code.split('-');
    expect(segments).toHaveLength(4);
    expect(segments[0]).toBe('FWGC');
    expect(segments[1]).toHaveLength(4);
    expect(segments[2]).toHaveLength(4);
    expect(segments[3]).toHaveLength(4);
  });

  it('excludes confusing characters (0, O, 1, I)', () => {
    // Generate many codes to ensure we never see excluded chars
    for (let i = 0; i < 100; i++) {
      const code = generateGiftCardCode();
      const body = code.replace('FWGC-', '').replace(/-/g, '');
      expect(body).not.toContain('0');
      expect(body).not.toContain('O');
      expect(body).not.toContain('1');
      expect(body).not.toContain('I');
    }
  });

  it('generates unique codes', () => {
    const codes = new Set<string>();
    for (let i = 0; i < 200; i++) {
      codes.add(generateGiftCardCode());
    }
    // With cryptographic randomness and 30^12 possibilities, all should be unique
    expect(codes.size).toBe(200);
  });

  it('only contains uppercase letters and digits in code body', () => {
    for (let i = 0; i < 50; i++) {
      const code = generateGiftCardCode();
      const body = code.replace('FWGC-', '').replace(/-/g, '');
      expect(body).toMatch(/^[A-Z2-9]+$/);
    }
  });
});

// =============================================
// isValidCodeFormat (validateGiftCardCode)
// =============================================
describe('isValidCodeFormat', () => {
  it('accepts valid format FWGC-XXXX-XXXX-XXXX', () => {
    expect(isValidCodeFormat('FWGC-ABCD-EFGH-JKLM')).toBe(true);
  });

  it('accepts codes with allowed digits 2-9', () => {
    expect(isValidCodeFormat('FWGC-2345-6789-ABCD')).toBe(true);
  });

  it('rejects codes with 0 (excluded char)', () => {
    expect(isValidCodeFormat('FWGC-A0CD-EFGH-JKLM')).toBe(false);
  });

  it('rejects codes with 1 (excluded char)', () => {
    expect(isValidCodeFormat('FWGC-A1CD-EFGH-JKLM')).toBe(false);
  });

  it('rejects codes with O (excluded char)', () => {
    expect(isValidCodeFormat('FWGC-AOCD-EFGH-JKLM')).toBe(false);
  });

  it('rejects codes with I (excluded char)', () => {
    expect(isValidCodeFormat('FWGC-AICD-EFGH-JKLM')).toBe(false);
  });

  it('normalizes lowercase to uppercase before validation', () => {
    expect(isValidCodeFormat('fwgc-abcd-efgh-jklm')).toBe(true);
  });

  it('normalizes mixed case', () => {
    expect(isValidCodeFormat('Fwgc-AbCd-EfGh-JkLm')).toBe(true);
  });

  it('trims whitespace', () => {
    expect(isValidCodeFormat('  FWGC-ABCD-EFGH-JKLM  ')).toBe(true);
  });

  it('rejects wrong prefix', () => {
    expect(isValidCodeFormat('GIFT-ABCD-EFGH-JKLM')).toBe(false);
  });

  it('rejects too few segments', () => {
    expect(isValidCodeFormat('FWGC-ABCD-EFGH')).toBe(false);
  });

  it('rejects too many segments', () => {
    expect(isValidCodeFormat('FWGC-ABCD-EFGH-JKLM-NPQR')).toBe(false);
  });

  it('rejects segments with wrong length', () => {
    expect(isValidCodeFormat('FWGC-ABC-EFGH-JKLM')).toBe(false);
    expect(isValidCodeFormat('FWGC-ABCDE-EFGH-JKLM')).toBe(false);
  });

  it('rejects empty string', () => {
    expect(isValidCodeFormat('')).toBe(false);
  });

  it('rejects random string', () => {
    expect(isValidCodeFormat('not-a-gift-card-code')).toBe(false);
  });

  it('validates codes generated by generateGiftCardCode', () => {
    for (let i = 0; i < 50; i++) {
      const code = generateGiftCardCode();
      expect(isValidCodeFormat(code)).toBe(true);
    }
  });
});

