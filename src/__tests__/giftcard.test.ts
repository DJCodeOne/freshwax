describe('createWelcomeGiftCard', () => {
  it('creates a gift card with correct defaults', () => {
    const gc = createWelcomeGiftCard('test@example.com');
    expect(gc.originalValue).toBe(50);
    expect(gc.currentBalance).toBe(50);
    expect(gc.type).toBe('welcome');
    expect(gc.isActive).toBe(true);
    expect(gc.redeemedBy).toBeNull();
    expect(gc.redeemedAt).toBeNull();
    expect(gc.createdFor).toBe('test@example.com');
  });

  it('generates a valid gift card code', () => {
    const gc = createWelcomeGiftCard();
    expect(isValidCodeFormat(gc.code)).toBe(true);
  });

  it('sets description mentioning welcome and amount', () => {
    const gc = createWelcomeGiftCard();
    expect(gc.description).toContain('50');
    expect(gc.description).toContain('Welcome');
  });

  it('sets expiry date approximately 1 year from now', () => {
    const gc = createWelcomeGiftCard();
    expect(gc.expiresAt).toBeTruthy();
    const expiry = new Date(gc.expiresAt!);
    const diffDays = (expiry.getTime() - Date.now()) / (1000 * 60 * 60 * 24);
    expect(diffDays).toBeGreaterThan(363);
  });

  it('sets createdAt to current time', () => {
    const before = Date.now();
    const gc = createWelcomeGiftCard();
    const after = Date.now();
    const created = new Date(gc.createdAt).getTime();
    expect(created).toBeGreaterThanOrEqual(before);
    expect(created).toBeLessThanOrEqual(after);
  });
});

