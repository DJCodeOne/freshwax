// createGiftCardAfterPayment
// =============================================
describe('createGiftCardAfterPayment', () => {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  let mockFirebaseOps: any;

  let originalFetch: typeof globalThis.fetch;
  let mockFetch: ReturnType<typeof vi.fn>;

  beforeEach(() => {
    mockFirebaseOps = {
      queryCollection: vi.fn().mockResolvedValue([]), // no existing codes
      addDocument: vi.fn().mockResolvedValue({ id: 'gc_123' }),
      updateDocument: vi.fn().mockResolvedValue(undefined),
      getDocument: vi.fn().mockResolvedValue(null),
    };

    originalFetch = globalThis.fetch;
    mockFetch = vi.fn();
    globalThis.fetch = mockFetch as typeof globalThis.fetch;

    // Mock the purchase record subcollection write
    mockFetch.mockResolvedValue({
      ok: true,
      json: () => Promise.resolve({}),
      text: () => Promise.resolve(''),
    });
  });

  afterEach(() => {
    globalThis.fetch = originalFetch;
  });

  it('creates a gift card successfully for a gift purchase', async () => {
    const data = {
      amount: 25,
      buyerUserId: 'buyer123',
      buyerEmail: 'buyer@example.com',
      buyerName: 'Test Buyer',
      recipientType: 'gift' as const,
      recipientName: 'Recipient',
      recipientEmail: 'recipient@example.com',
      message: 'Happy birthday!',
      paymentIntentId: 'pi_123',
    };

    const result = await createGiftCardAfterPayment(data, mockFirebaseOps);

    expect(result.success).toBe(true);
    expect(result.giftCard).toBeDefined();
    expect(result.giftCard!.amount).toBe(25);
    expect(result.giftCard!.recipientEmail).toBe('recipient@example.com');
    expect(result.giftCard!.code).toBeTruthy();
    expect(isValidCodeFormat(result.giftCard!.code)).toBe(true);
  });

  it('creates a self-purchase gift card', async () => {
    const data = {
      amount: 50,
      buyerUserId: 'buyer123',
      buyerEmail: 'buyer@example.com',
      buyerName: 'Self Buyer',
      recipientType: 'self' as const,
      recipientName: 'Self Buyer',
      recipientEmail: 'buyer@example.com',
      message: '',
    };

    const result = await createGiftCardAfterPayment(data, mockFirebaseOps);

    expect(result.success).toBe(true);
    expect(result.giftCard!.recipientEmail).toBe('buyer@example.com');
  });

  it('generates unique code by retrying on collision', async () => {
    // First query returns existing code, subsequent queries return empty
    mockFirebaseOps.queryCollection
      .mockResolvedValueOnce([{ code: 'EXISTING' }]) // collision
      .mockResolvedValueOnce([{ code: 'EXISTING2' }]) // collision again
      .mockResolvedValueOnce([]); // unique

    const data = {
      amount: 10,
      buyerUserId: 'buyer123',
      buyerEmail: 'buyer@example.com',
      buyerName: 'Test',
      recipientType: 'self' as const,
      recipientName: 'Test',
      recipientEmail: 'buyer@example.com',
      message: '',
    };

    const result = await createGiftCardAfterPayment(data, mockFirebaseOps);
    expect(result.success).toBe(true);
    // queryCollection should have been called 3 times for code uniqueness
    expect(mockFirebaseOps.queryCollection).toHaveBeenCalledTimes(3);
  });

  it('stores gift card document in Firestore', async () => {
    const data = {
      amount: 30,
      buyerUserId: 'buyer123',
      buyerEmail: 'buyer@example.com',
      buyerName: 'Buyer',
      recipientType: 'gift' as const,
      recipientName: 'Recipient',
      recipientEmail: 'recipient@example.com',
      message: 'Enjoy!',
      paymentIntentId: 'pi_abc',
    };

    await createGiftCardAfterPayment(data, mockFirebaseOps);

    expect(mockFirebaseOps.addDocument).toHaveBeenCalledWith(
      'giftCards',
      expect.objectContaining({
        originalValue: 30,
        currentBalance: 30,
        isActive: true,
        recipientEmail: 'recipient@example.com',
        purchasedBy: 'buyer123',
        paymentIntentId: 'pi_abc',
      })
    );
  });

  it('looks up buyer name from user doc when not provided', async () => {
    mockFirebaseOps.getDocument.mockResolvedValueOnce({
      displayName: 'DB User Name',
    });

    const data = {
      amount: 15,
      buyerUserId: 'buyer123',
      buyerEmail: 'buyer@example.com',
      buyerName: '', // empty - should look up from DB
      recipientType: 'self' as const,
      recipientName: 'Self',
      recipientEmail: 'buyer@example.com',
      message: '',
    };

    await createGiftCardAfterPayment(data, mockFirebaseOps);

    expect(mockFirebaseOps.getDocument).toHaveBeenCalledWith('users', 'buyer123');
  });

  it('returns error on failure', async () => {
    mockFirebaseOps.addDocument.mockRejectedValueOnce(new Error('DB error'));

    const data = {
      amount: 10,
      buyerUserId: 'buyer123',
      buyerEmail: 'buyer@example.com',
      buyerName: 'Test',
      recipientType: 'self' as const,
      recipientName: 'Test',
      recipientEmail: 'buyer@example.com',
      message: '',
    };

    const result = await createGiftCardAfterPayment(data, mockFirebaseOps);
    expect(result.success).toBe(false);
    expect(result.error).toBeDefined();
  });

  it('handles PayPal order ID', async () => {
    const data = {
      amount: 20,
      buyerUserId: 'buyer123',
      buyerEmail: 'buyer@example.com',
      buyerName: 'Buyer',
      recipientType: 'self' as const,
      recipientName: 'Buyer',
      recipientEmail: 'buyer@example.com',
      message: '',
      paypalOrderId: 'PAYPAL-ORDER-123',
    };

    await createGiftCardAfterPayment(data, mockFirebaseOps);

    expect(mockFirebaseOps.addDocument).toHaveBeenCalledWith(
      'giftCards',
      expect.objectContaining({
        paypalOrderId: 'PAYPAL-ORDER-123',
        paymentIntentId: null, // should be null when not provided
      })
    );
  });
});
