// =============================================
// getDefaultExpiry
// =============================================
describe('getDefaultExpiry', () => {
  it('returns a valid ISO date string', () => {
    const expiry = getDefaultExpiry();
    expect(() => new Date(expiry)).not.toThrow();
    expect(new Date(expiry).toISOString()).toBe(expiry);
  });

  it('returns a date approximately 1 year from now', () => {
    const now = new Date();
    const expiry = new Date(getDefaultExpiry());
    const diffMs = expiry.getTime() - now.getTime();
    const diffDays = diffMs / (1000 * 60 * 60 * 24);
    // Should be between 364 and 367 days (accounting for leap years and timing)
    expect(diffDays).toBeGreaterThan(363);
    expect(diffDays).toBeLessThan(368);
  });
});

// =============================================
// isExpired
// =============================================
describe('isExpired', () => {
  it('returns false for null expiresAt (no expiry)', () => {
    expect(isExpired(null)).toBe(false);
  });

  it('returns false for future date', () => {
    const future = new Date();
    future.setFullYear(future.getFullYear() + 1);
    expect(isExpired(future.toISOString())).toBe(false);
  });

  it('returns true for past date', () => {
    const past = new Date();
    past.setFullYear(past.getFullYear() - 1);
    expect(isExpired(past.toISOString())).toBe(true);
  });
});

